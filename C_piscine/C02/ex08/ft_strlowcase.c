char	*ft_strlowcase(char *str)
{
	int	b;

	b = 0;
	while (str[b] != '\0')
	{
		if ((str[b] >= 'A') && (str[b] <= 'Z'))
			str[b] = str[b] + 32;
			b++;
	}
	return (str);
}
